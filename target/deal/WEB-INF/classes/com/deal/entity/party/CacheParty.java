package com.deal.entity.party;

import java.io.Serializable;

public class CacheParty implements Serializable{

	private static final long serialVersionUID = 1L;

	private int connectState;
	private String phoneNumber;
	private String partyId;
	private int partyRole;
	private int disconnectReason;
	private long inConfTime;
	private long connectTime;

	public int getPartyRole(){
		return partyRole;
	}

	public void setPartyRole(int partyRole){
		this.partyRole = partyRole;
	}

	public String getPartyId(){
		return partyId;
	}

	public void setPartyId(String partyId){
		this.partyId = partyId;
	}

	public int getConnectState(){
		return connectState;
	}

	public void setConnectState(int connectState){
		this.connectState = connectState;
	}

	public String getPhoneNumber(){
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber){
		this.phoneNumber = phoneNumber;
	}

	public int getDisconnectReason(){
		return disconnectReason;
	}

	public void setDisconnectReason(int disconnectReason){
		this.disconnectReason = disconnectReason;
	}

	public long getInConfTime(){
		return inConfTime;
	}

	public void setInConfTime(long inConfTime){
		this.inConfTime = inConfTime;
	}

	public long getConnectTime(){
		return connectTime;
	}

	public void setConnectTime(long connectTime){
		this.connectTime = connectTime;
	}
}
