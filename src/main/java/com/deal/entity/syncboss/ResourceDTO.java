package com.deal.entity.syncboss;


public class ResourceDTO {
	private Integer siteId;
	private String siteURL;
	private String hostPassword;
	private String guestPassword;
	
	public ResourceDTO(){}
	
	public ResourceDTO(Integer siteId, String siteURL, String hostPassword, String guestPassword) {
		super();
		this.siteId = siteId;
		this.siteURL = siteURL;
		this.hostPassword = hostPassword;
		this.guestPassword = guestPassword;
	}

	public Integer getSiteId() {
		return siteId;
	}

	public void setSiteId(Integer siteId) {
		this.siteId = siteId;
	}

	/**
	 * @return the siteURL
	 */
	public String getSiteURL() {
		return siteURL;
	}
	
	/**
	 * @param siteURL the siteURL to set
	 */
	public void setSiteURL(String siteURL) {
		this.siteURL = siteURL;
	}
	
	/**
	 * @return the hostPassword
	 */
	public String getHostPassword() {
		return hostPassword;
	}
	
	/**
	 * @param hostPassword the hostPassword to set
	 */
	public void setHostPassword(String hostPassword) {
		this.hostPassword = hostPassword;
	}
	
	/**
	 * @return the guestPassword
	 */
	public String getGuestPassword() {
		return guestPassword;
	}
	
	/**
	 * @param guestPassword the guestPassword to set
	 */
	public void setGuestPassword(String guestPassword) {
		this.guestPassword = guestPassword;
	}
}

